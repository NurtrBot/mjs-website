export function getTaxRate(zip: string): number {
  if (!zip) return 0.0775;
  const clean = zip.trim().slice(0, 5);
  if (EXACT_ZIP_RATES[clean]) return EXACT_ZIP_RATES[clean] / 100;
  const prefix = clean.slice(0, 3);
  if (PREFIX_RATES[prefix]) return PREFIX_RATES[prefix] / 100;
  return 0.0775;
}

export function formatTaxRate(zip: string): string {
  const rate = getTaxRate(zip);
  return `${(rate * 100).toFixed(2).replace(/\.?0+$/, '')}%`;
}
